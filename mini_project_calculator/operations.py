
# Python module that includes all basic arithmetic
# operations

def add( x , y ):
    """
    Adds x and y
    """
    return x + y

def subtract( x , y ):
    """
    Subtracts y from x
    """
    return x - y

def multiply( x , y ):
    """
    Multiplies x by y
    """
    return x * y

def divide( x , y ):
    """
    Divides x and y
    """
    return x / y

def floor_divide( x , y ):
    """
    Returns floor( x / y )
    """
    return x // y

def remainder( x , y ):
    """
    Returns remainder of x / y
    """
    return x % y

def power( x , y ):
    """
    Computes x to the power y
    """
    return x ** y